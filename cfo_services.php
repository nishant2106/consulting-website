                    <h4>Methods of Valuation</h4>
                    <p>We Acco Global follow different business valuation methods that shall be preferable in different
                        scenarios for different Industries. Generally, the best approach will depend on why the
                        valuation is needed, the size of your business, your industry, and other factors.</p>
                    <p>There are numerous ways a company can be valued:</p>
                    <p>
                        <b>1. Market Capitalization</b>
                        The most basic approach of valuing a company is to use market capitalization. It's computed by
                        dividing the company's share price by the total number of outstanding shares.
                    </p>
                    <p><b>2. Times Revenue Method</b>
                        A stream of revenues generated over a period of time is applied to a multiplier that relies on
                        the industry and economic climate in the times revenue business valuation approach. </p>
                    <p><b>3. Earnings Multiplier</b>&nbsp;&nbsp;The earnings multiplier, rather than the time revenue
                        technique, can be used to acquire a more
                        accurate representation of a company's real value, because profits are a more reliable predictor
                        of financial performance than sales revenue. The earnings multiplier compares future profits to
                        cash flow that might be invested over the same time period at the present interest rate. To put
                        it
                        another way, it adjusts the current P/E ratio to take current interest rates into account.</p>
                    <p><b>4. Discounted Cash Flow (DCF) Method</b>&nbsp;&nbsp;&nbsp;The earnings multiplier is
                        comparable to the DCF approach of business valuation. This strategy is based on future cash flow
                        estimates that are updated to determine the company's current market value. The fundamental
                        difference between the discounted cash flow approach and the earning
                        multiplier method is that the discounted cash flow method calculates the present value after
                        taking inflation into account.</p>
                    <p><b>5. Book Value</b>&nbsp;&nbsp;&nbsp;The worth of a company's shareholders' equity as
                        represented on the balance sheet statement. A company's book value is calculated by subtracting
                        its total liabilities from its total assets.</p>
                    <p><b>6. Liquidation Value</b>&nbsp;&nbsp;&nbsp;The net cash that a company would receive if its
                        assets were liquidated and its liabilities were paid off today is known as liquidation value.
                    </p>
